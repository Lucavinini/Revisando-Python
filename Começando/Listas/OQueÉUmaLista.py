#Lista é uma coleção de elementos, organizados de forma linear e enumerados por indices.
#0 - infinito.

#Em python, [] indicam uma lista.

#Ex.:

Comida = ["bolo", "Laranja", "Maça"] #Os elementos são separados por virgula

print(Comida)

#Para Printar um único item:

print(Comida[0])

#Pode-se usar formatações por meio dos métodos

print(Comida[0].title())

#Pode-se acessar os últimos elementos da lista usando a notação de números negativos
#para acessar a lista ao contrário. 

print(Comida[-1])

print(Comida[1])


